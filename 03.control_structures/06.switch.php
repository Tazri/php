<?php

$number = 5;

// basic switch in php : 

switch($number){
    case 2 : 
        echo "number is two\n";
        break;
    case 3 : 
        echo "number is three\n";
        break;
    case 4 :
        echo "number is four\n";
        break;
    case 5 : 
        echo "number is five\n";
        break;
    default : 
        echo "number is unknown\n";
}

?>